def overall_summary(
    project_metrics: dict,
    axis_scores: dict | None,
    overall_score: float | None,
    state_text: str | None,
    artifacts_pct: float,
) -> list[str]:
    """Краткое словесное резюме сессии (3–6 строк), на основе показателей из справочника."""
    lines: list[str] = []

    # Quality gate первым
    art_label, art_meaning = ARTIFACTS.interpret(artifacts_pct)
    if artifacts_pct > 20:
        lines.append(
            f"⚠ Качество сигнала: {art_label} ({artifacts_pct:.0f}% артефактов) — {art_meaning}. "
            "Остальным числам ниже доверять нельзя."
        )
        return lines

    if overall_score is not None and state_text:
        lines.append(f"Общий балл готовности — {int(overall_score)}/100, состояние «{state_text}».")

    rmssd = project_metrics.get("rmssd")
    sdnn = project_metrics.get("sdnn")
    si = project_metrics.get("stress_index")
    lfhf = project_metrics.get("lf_hf_ratio")
    mean_rr = project_metrics.get("mean_rr")

    if rmssd is not None:
        lab, mean = RMSSD.interpret(rmssd)
        lines.append(f"Парасимпатика (RMSSD = {rmssd:.0f} мс): {lab} — {mean}.")
    if sdnn is not None:
        lab, mean = SDNN.interpret(sdnn)
        lines.append(f"Общая вариабельность (SDNN = {sdnn:.0f} мс): {lab} — {mean}.")
    if si is not None:
        lab, mean = STRESS_INDEX.interpret(si)
        lines.append(f"Индекс напряжения = {si:.0f}: {lab} — {mean}.")
    if lfhf is not None:
        lab, mean = LF_HF.interpret(lfhf)
        lines.append(f"LF/HF = {lfhf:.2f}: {lab} — {mean}.")
    if mean_rr is not None:
        hr = 60000.0 / mean_rr
        lines.append(f"Средний пульс ≈ {hr:.0f} уд/мин.")

    if 10 < artifacts_pct <= 20:
        lines.append(f"⚠ Качество сигнала: {art_label} ({artifacts_pct:.0f}% артефактов). Выводы — с оговоркой.")

    return lines


def hypotheses_for_change(prev: dict, curr: dict) -> list[str]:
    """Генерирует возможные объяснения изменения метрик между двумя сессиями.

    prev / curr — dict с ключами rmssd, sdnn, stress_index, lf_hf_ratio, mean_rr, artifacts_pct.
    """
    hyps: list[str] = []

    def pct_change(p, c):
        if p is None or c is None or p == 0:
            return None
        return (c - p) / p * 100.0

    # Качество — главное
    a_prev = prev.get("artifacts_pct")
    a_curr = curr.get("artifacts_pct")
    if a_curr is not None and a_curr > 15:
        hyps.append(f"Сейчас {a_curr:.0f}% артефактов — возможно, ты двигалась, жестикулировала или датчик слабо сидит.")
    elif a_prev is not None and a_curr is not None and a_curr - a_prev > 10:
        hyps.append(f"Шума стало больше (+{a_curr - a_prev:.0f}%) — проверь посадку датчика.")

    rmssd_chg = pct_change(prev.get("rmssd"), curr.get("rmssd"))
    if rmssd_chg is not None:
        if rmssd_chg < -25:
            hyps.append(f"RMSSD упал на {abs(rmssd_chg):.0f}% — возможно: стресс, плотный приём пищи, кофеин/алкоголь, плохо спала.")
        elif rmssd_chg > 30:
            hyps.append(f"RMSSD вырос на {rmssd_chg:.0f}% — возможно: глубокая релаксация, медленное дыхание, либо вернулась после нагрузки.")

    si_chg = pct_change(prev.get("stress_index"), curr.get("stress_index"))
    if si_chg is not None and si_chg > 50:
        hyps.append(f"Stress Index вырос в {1 + si_chg/100:.1f}× — стрессовое событие, эмоциональная реакция или физическая нагрузка.")
    elif si_chg is not None and si_chg < -40:
        hyps.append(f"Stress Index упал на {abs(si_chg):.0f}% — снизилась нагрузка, перешла к более спокойной деятельности.")

    lfhf_chg = pct_change(prev.get("lf_hf_ratio"), curr.get("lf_hf_ratio"))
    if lfhf_chg is not None and abs(lfhf_chg) > 50:
        direction = "симпатику (мобилизация)" if lfhf_chg > 0 else "парасимпатику (восстановление)"
        hyps.append(f"LF/HF сместился в сторону {direction} на {abs(lfhf_chg):.0f}%.")

    rr_curr = curr.get("mean_rr")
    rr_prev = prev.get("mean_rr")
    if rr_prev and rr_curr:
        hr_prev = 60000.0 / rr_prev
        hr_curr = 60000.0 / rr_curr
        if hr_curr - hr_prev > 10:
            hyps.append(f"Пульс вырос с {hr_prev:.0f} до {hr_curr:.0f} — активная деятельность, кофе или стресс.")
        elif hr_prev - hr_curr > 10:
            hyps.append(f"Пульс снизился с {hr_prev:.0f} до {hr_curr:.0f} — расслабление, сон, восстановление.")

    if not hyps:
        hyps.append("Существенных сдвигов нет — состояние стабильное.")
    return hyps
