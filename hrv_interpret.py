def biofeedback_recommendations(metrics: dict, artifacts_pct: float = 0.0) -> list[dict]:
    """Генерирует 1–3 конкретные рекомендации «что сделать сейчас».

    Каждая рекомендация: {level: 'good|info|action|alert', text: '...', why: '...'}
    metrics — плоский dict с rmssd, sdnn, stress_index, lf_hf_ratio, mean_rr, hf_power, lf_power.
    """
    recs: list[dict] = []

    if artifacts_pct and artifacts_pct > 15:
        return [{
            "level": "alert",
            "text": f"Сначала качество сигнала: {artifacts_pct:.0f}% артефактов. Подвинь датчик, прижми ремешок плотнее.",
            "why": "при шумном сигнале любые рекомендации ниже бесполезны",
        }]

    rmssd = metrics.get("rmssd")
    sdnn = metrics.get("sdnn")
    si = metrics.get("stress_index")
    lfhf = metrics.get("lf_hf_ratio")
    mean_rr = metrics.get("mean_rr")
    hf = metrics.get("hf_power")

    hr = (60000.0 / mean_rr) if mean_rr else None

    # Симпатический перекос
    if lfhf is not None and lfhf > 2.5:
        recs.append({
            "level": "action",
            "text": "Включи медленное дыхание: вдох 4 сек, выдох 6 сек, 3 минуты. Это переключит баланс в сторону восстановления.",
            "why": f"LF/HF = {lfhf:.1f} (>2.5) — симпатическая активность преобладает",
        })

    # Высокий стресс-индекс
    if si is not None and si > 200:
        recs.append({
            "level": "action",
            "text": "Сделай паузу на 5 минут: встань, разомни плечи, опусти их, расслабь челюсть. Это снимет острое напряжение.",
            "why": f"Stress Index = {si:.0f} (>200) — повышенное напряжение регуляторных систем",
        })

    # Низкая ПНС
    if rmssd is not None and rmssd < 19:
        recs.append({
            "level": "action",
            "text": "Парасимпатика снижена — попробуй технику «4–7–8»: вдох 4 сек, задержка 7 сек, выдох 8 сек. 4 цикла.",
            "why": f"RMSSD = {rmssd:.0f} мс (<19) — низкая способность к саморегуляции",
        })

    # Высокий пульс в покое
    if hr is not None and hr > 90:
        recs.append({
            "level": "info",
            "text": f"Пульс {hr:.0f} — выше обычного покоя. Если не двигалась — возможно кофеин, тревога или плохой сон.",
            "why": "HR > 90 в покое часто связан с активацией СНС",
        })

    # Низкая общая вариабельность
    if sdnn is not None and sdnn < 30:
        recs.append({
            "level": "alert",
            "text": "Общая адаптация заметно снижена. Сегодня лучше избегать сложных задач, дать себе передышку.",
            "why": f"SDNN = {sdnn:.0f} мс (<30) — критическое снижение адаптационных ресурсов",
        })

    # Всё хорошо
    if not recs:
        if rmssd and rmssd >= 25 and (lfhf is None or 0.5 <= lfhf <= 2.0) and (si is None or si <= 150):
            recs.append({
                "level": "good",
                "text": "Состояние сбалансированное. Хорошее время для сложных задач или сосредоточенной работы.",
                "why": "RMSSD в норме, LF/HF в балансе, стресс умеренный",
            })
        else:
            recs.append({
                "level": "info",
                "text": "Состояние в пределах нормы, выраженных отклонений нет.",
                "why": "ни одна метрика не выходит за пороги внимания",
            })

    return recs[:3]


def overall_picture(metrics: dict, axis_scores: dict | None = None) -> str:
    """Развёрнутое описание состояния (несколько предложений) с учётом связей между метриками."""
    rmssd = metrics.get("rmssd")
    si = metrics.get("stress_index")
    lfhf = metrics.get("lf_hf_ratio")
    hr = (60000.0 / metrics.get("mean_rr")) if metrics.get("mean_rr") else None
    sdnn = metrics.get("sdnn")
    pnn50 = metrics.get("pnn50")
    hf = metrics.get("hf_power")
    lf = metrics.get("lf_power")
    dfa1 = metrics.get("dfa_alpha1")

    if not any(v is not None for v in (rmssd, si, lfhf, sdnn)):
        return "Недостаточно данных для комплексной оценки."

    sentences: list[str] = []

    # 1. Вегетативный режим — как работают СНС и ПНС, и что это значит
    if lfhf is not None and rmssd is not None:
        if lfhf < 0.5:
            mode = ("Вегетативная нервная система в режиме глубокого восстановления: преобладает "
                    f"парасимпатика (LF/HF = {lfhf:.2f}), RMSSD = {rmssd:.0f} мс. "
                    "Это состояние хорошо подходит для творческих задач, медитации, восстановительных практик, "
                    "но не оптимально для задач, требующих быстрой мобилизации.")
        elif lfhf < 2.0:
            mode = (f"Вегетативный баланс оптимальный (LF/HF = {lfhf:.2f}, RMSSD = {rmssd:.0f} мс): "
                    "симпатика и парасимпатика работают согласованно. "
                    "Это рабочее состояние, годное для большинства типов задач — концентрации, общения, принятия решений.")
        elif lfhf < 4.0:
            mode = (f"ВНС в режиме мобилизации (LF/HF = {lfhf:.2f}): симпатика преобладает над парасимпатикой. "
                    f"При RMSSD = {rmssd:.0f} мс ")
            if rmssd >= 30:
                mode += "у организма ещё есть восстановительный резерв, но если такое состояние держится долго — будет утомление."
            else:
                mode += "восстановительные ресурсы уже сниженные — пора сделать паузу."
        else:
            mode = (f"ВНС в состоянии острого напряжения (LF/HF = {lfhf:.2f}, выраженный симпатический сдвиг). "
                    "Эффективность когнитивных задач в этом режиме падает, повышается импульсивность решений.")
        sentences.append(mode)

    # 2. Ресурсы — комплексно по SDNN + RMSSD + pNN50
    if sdnn is not None and rmssd is not None:
        if sdnn >= 50 and rmssd >= 25:
            res = (f"Регуляторные ресурсы в норме: общая вариабельность {sdnn:.0f} мс, "
                   "адаптационных резервов достаточно для нагрузок в течение дня")
            if pnn50 is not None:
                if pnn50 >= 15:
                    res += f"; нервная система гибкая (pNN50 = {pnn50:.0f}%) — хорошо переключается между задачами."
                else:
                    res += "."
        elif sdnn >= 30 and rmssd >= 19:
            res = (f"Регуляторные ресурсы умеренные (SDNN = {sdnn:.0f} мс): "
                   "рутинные задачи доступны, но сложные / длительные — на пределе. Стоит избегать пиковых нагрузок.")
        else:
            res = (f"Регуляторные ресурсы заметно снижены (SDNN = {sdnn:.0f} мс, RMSSD = {rmssd:.0f} мс): "
                   "это состояние утомления, стресса или плохого восстановления. Сегодня лучше дать себе передышку.")
        sentences.append(res)

    # 3. Острое напряжение — отдельной фразой про SI
    if si is not None:
        if si < 30:
            si_txt = (f"Индекс напряжения Баевского = {si:.0f} — очень низкий (ваготония). "
                      "Регуляция «провисла» в сторону покоя; это может быть нормой для тренированных людей в покое, "
                      "но и признаком гипотонии или слабости тонуса. В работе — снижение мобилизации.")
        elif si <= 150:
            si_txt = f"Индекс Баевского = {si:.0f} — нормальный рабочий тонус, признаков острого стресса нет."
        elif si <= 400:
            si_txt = (f"Индекс Баевского = {si:.0f} — повышенное напряжение. "
                      "Регуляторные системы работают «с надбавкой» — короткая пауза поможет.")
        else:
            si_txt = (f"Индекс Баевского = {si:.0f} — критическое перенапряжение. "
                      "Это уже не рабочий стресс, а сигнал на отдых.")
        sentences.append(si_txt)

    # 4. Активация по HR
    if hr is not None:
        if hr > 95:
            sentences.append(f"Пульс {hr:.0f} уд/мин — высокий для покоя. Если ты не двигалась — возможны кофеин, тревога, недосып или физическая нагрузка прямо перед записью.")
        elif hr < 55:
            sentences.append(f"Пульс {hr:.0f} уд/мин — низкий. Это нормально для тренированных или в глубоком покое.")

    # 5. Качество регуляции — по DFA
    if dfa1 is not None:
        if dfa1 < 0.75:
            sentences.append(f"DFA α1 = {dfa1:.2f} — ритм слишком случайный, регуляция нарушена.")
        elif dfa1 > 1.3:
            sentences.append(f"DFA α1 = {dfa1:.2f} — ритм избыточно регулярный, признак напряжения / возрастной жёсткости.")

    return " ".join(sentences)


def stability_analysis(rr_ms_list: list[float], window_sec: int = 300, step_sec: int = 60) -> dict:
    """Анализ стабильности метрик внутри одной записи.

    Разбивает запись на скользящие 5-минутные окна, считает RMSSD/SDNN/HR в каждом,
    выдаёт CV (коэффициент вариации) — это показывает, какие метрики «гуляют»,
    а какие у этого человека стабильны.

    Возвращает {metric: {mean, std, cv_pct, values: [...]}}
    """
    import numpy as np
    rr = np.asarray(rr_ms_list, dtype=float)
    if len(rr) < 30:
        return {}

    cum_sec = np.cumsum(rr) / 1000.0
    total = float(cum_sec[-1])

    series = {"RMSSD": [], "SDNN": [], "HR": [], "MeanRR": []}
    t_start = 0.0
    while t_start + window_sec <= total:
        mask = (cum_sec >= t_start) & (cum_sec < t_start + window_sec)
        rr_w = rr[mask]
        if len(rr_w) >= 30:
            series["RMSSD"].append(float(np.sqrt(np.mean(np.diff(rr_w) ** 2))))
            series["SDNN"].append(float(np.std(rr_w, ddof=1)))
            mean_rr = float(np.mean(rr_w))
            series["HR"].append(float(60000.0 / mean_rr))
            series["MeanRR"].append(mean_rr)
        t_start += step_sec

    out = {}
    for name, vals in series.items():
        if len(vals) < 2:
            continue
        v = np.array(vals)
        mu = float(v.mean())
        sd = float(v.std(ddof=1))
        cv = float(100.0 * sd / mu) if mu else 0.0
        out[name] = {
            "mean": round(mu, 1),
            "std": round(sd, 1),
            "cv_pct": round(cv, 1),
            "min": round(float(v.min()), 1),
            "max": round(float(v.max()), 1),
            "n_windows": int(len(v)),
            "values": [round(x, 1) for x in vals],
        }

    # Интерпретация: какие метрики стабильны, какие гуляют
    interp = []
    for name, st in out.items():
        if st["cv_pct"] < 5:
            interp.append(f"{name} стабилен (CV {st['cv_pct']}%) — это твоя характеристика, мало меняется внутри сессии.")
        elif st["cv_pct"] < 15:
            interp.append(f"{name} умеренно гуляет (CV {st['cv_pct']}%) — реагирует на состояние, но не резко.")
        else:
            interp.append(f"{name} сильно меняется (CV {st['cv_pct']}%) — этот показатель сильно зависит от текущего состояния (нагрузка, эмоции, дыхание).")

    return {"metrics": out, "interpretation": interp}


