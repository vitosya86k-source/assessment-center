"""
hrv_interpret.py — словарь показателей HRV, точно по «Справочник.pdf».

Каждый показатель содержит:
  name        — русское/международное название
  unit        — единицы измерения
  description — определение
  applicability — применимость в нейроассессменте (что значит этот показатель для оценки)
  ranges      — список диапазонов (lo, hi, label, meaning)
  age         — возрастные особенности (опционально)
  medical     — медицинские факторы (опционально)
"""

from __future__ import annotations

from dataclasses import dataclass, field
from typing import Optional


@dataclass
class MetricSpec:
    code: str
    name: str
    unit: str
    description: str
    applicability: str
    ranges: list[tuple[float, float, str, str]]
    age: str = ""
    medical: str = ""

    def interpret(self, value: Optional[float]) -> tuple[str, str]:
        if value is None or (isinstance(value, float) and value != value):
            return ("—", "недостаточно данных")
        for lo, hi, label, meaning in self.ranges:
            if lo <= value < hi:
                return (label, meaning)
        return ("—", "вне ожидаемого диапазона")

    @property
    def norm_text(self) -> str:
        for lo, hi, label, _ in self.ranges:
            if "норма" in label.lower() or "баланс" in label.lower():
                hi_disp = "+∞" if hi >= 99999 else str(hi)
                return f"{lo}–{hi_disp} {self.unit}".strip()
        return "—"


